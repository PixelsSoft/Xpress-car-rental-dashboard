import { toast } from 'react-toastify'

export const successNotify = (mesg) => {
    toast.success(mesg, {
        position: "bottom-center",
        autoClose: 4000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
    });
}

